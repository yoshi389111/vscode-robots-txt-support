/** Indicates the support level for a directive or feature. */
export type SupportLevel = "supported" | "ignored" | "unknown";

/**
 * Defines the structure for crawler information and a list of known crawlers with their details.
 */
export interface CrawlerInfo {
  /** Name of the crawler. */
  name: string;
  /** Description of the crawler. */
  description: string;
  /** Notice or additional information about the crawler. */
  notice?: string;
  /** URL for more information about the crawler. */
  url: string | undefined;
  /** Prefix of completion item. lowercase */
  prefix: Lowercase<string>;
  /** A placeholder for completion information. */
  isPlaceholder: boolean;
  /** Whether to hide completion items for the crawler. */
  hiddenCompletion: boolean;
  // TODO:
  // details: {
  //   operator: string;
  //   tags: string[];
  // };
  /** Indicates the support levels for various directives and features. */
  supportLevels: {
    /** Indicates the support level for the `robots.txt` file. */
    robotsTxt: SupportLevel;
    /** Indicates the support level for the `Allow` directive. */
    allowDirective: SupportLevel;
    /** Indicates the support level for the `Crawl-delay` directive. */
    crawlDelayDirective: SupportLevel;
    /** Indicates the support level for the `Sitemap` directive. */
    sitemapDirective: SupportLevel;
    /** Indicates the support level for wildcard user agents. */
    wildcardUserAgent: SupportLevel;
    /** List of additional directives supported by the crawler. */
    additionalDirectives: string[];
    /** List of fallback user agents for the crawler. */
    fallbackUserAgents: string[] | undefined;
  };
}

